package com.fantingame.pay.entity;

public class PayMask {
	
	Integer id;
	String maskCondition;
	String maskPayChannel;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMaskCondition() {
		return maskCondition;
	}
	public void setMaskCondition(String maskCondition) {
		this.maskCondition = maskCondition;
	}
	public String getMaskPayChannel() {
		return maskPayChannel;
	}
	public void setMaskPayChannel(String maskPayChannel) {
		this.maskPayChannel = maskPayChannel;
	}
	
	
}
